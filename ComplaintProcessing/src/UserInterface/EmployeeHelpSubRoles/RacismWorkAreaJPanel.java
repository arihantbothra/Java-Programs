    public void populateRacismTable(){
      DefaultTableModel model = (DefaultTableModel)tblRacism.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : racismDepartmentOrganization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getEmployeeName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getEmployeeName();
            row[3] = request.getStatus();
            
            model.addRow(row);
        }
}

