    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        int selectedRow = tblRacism.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"please select a row");
            return;
            
        }
        LabTestWorkRequest selrequest=new LabTestWorkRequest();
        WorkRequest request = (WorkRequest)tblRacism.getValueAt(selectedRow, 0);
        selrequest.setTestResult((String) comboBoxSolution.getSelectedItem());
        request.setStatus("Completed");   
        request.setResolveDate(Date.from(Instant.now()));
        populateRacismTable();
        JOptionPane.showMessageDialog(null,"request completed");
// TODO add your handling code here:
    }//GEN-LAST:event_btnSubmitActionPerformed


