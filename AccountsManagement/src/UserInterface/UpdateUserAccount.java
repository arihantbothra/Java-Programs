    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String name = person.getText();
        String pwd = password.getText();
        String position = role.getSelectedItem().toString();
        try{
            active.setActionCommand("Active");
            unactive.setActionCommand("Unactive");
            for(UserAccount ua : business.getUa().getUseraccount()){
                if(ua.getUserName().equals(uname)){
                    ua.setName(name);
                    ua.setPassword(pwd);
                    try {
                        ua.setHashpassword(pwd);
                    } catch (NoSuchAlgorithmException ex) {
                        Logger.getLogger(UpdateUserAccount.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    ua.setRole(position);
                    ua.setStatus(status.getSelection().getActionCommand());
                }
            }
            for(PersonList pl : business.getPd().getPerson()){
                if(pl.getUsername().equals(uname)){
                    pl.setPerson(name);
                    pl.setStatus(status.getSelection().getActionCommand());
                }
            }

            JOptionPane.showMessageDialog(null, "Person Profile Updated Successfully");
            panel.remove(this);
            CardLayout layout = (CardLayout)panel.getLayout();
            layout.previous(panel);
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_updateActionPerformed

