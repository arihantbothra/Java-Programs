    private void newbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newbtnActionPerformed
        String name = person.getSelectedItem().toString();
        String position = role.getSelectedItem().toString();
        String user = username.getText();
        String pwd = password.getText();
        try{
        for(UserAccount ua : business.getUa().getUseraccount()){
            if(ua.getPerson().equalsIgnoreCase(name)){
                ua.setUserName(user);
                ua.setPassword(pwd);
                try {
                    ua.setHashpassword(pwd);
                } catch (NoSuchAlgorithmException ex) {
                    Logger.getLogger(NewUserAccount.class.getName()).log(Level.SEVERE, null, ex);
                }
                ua.setRole(position);
            }
        }
        for(PersonList pl : business.getPd().getPerson()){
            if(pl.toString().equalsIgnoreCase(name)){
                pl.setUsername(user);
            }
        }
        JOptionPane.showMessageDialog(null, "User Created Successfully");
        panel.remove(this);
        CardLayout layout = (CardLayout)panel.getLayout();
        layout.previous(panel);
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_newbtnActionPerformed

    private void personActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_personActionPerformed
        String name = person.getSelectedItem().toString();
        for(UserAccount ua : business.getUa().getUseraccount()){
            if(ua.getName().equals(name)){
                username.setText(ua.getUserName());
                username.setEditable(false);
            }
        }
    }//GEN-LAST:event_personActionPerformed


