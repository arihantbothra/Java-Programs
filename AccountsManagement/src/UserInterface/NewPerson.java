    private void newbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newbtnActionPerformed
        String fname = first.getText();
        String lname = last.getText();
        String user = username.getText();
        try{
            UserAccount ua = business.getUa().adduser();
            ua.setName(fname + " " + lname);
            ua.setUserName(user);
            ua.setStatus("Active");
            PersonList pl = business.getPd().addperson();
            pl.setFirstname(fname);
            pl.setLastname(lname);
            pl.setUsername(user);
            pl.setStatus("Active");

            JOptionPane.showMessageDialog(null, "Person Added Successfully");
            panel.remove(this);
            CardLayout layout = (CardLayout)panel.getLayout();
            layout.previous(panel);
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_newbtnActionPerformed

